function city_country(city, country) {
    return "".concat(city, ", ").concat(country); // if we describe value with specific data type so use return,and this method.
}
// calling back the function
city_country("Karachi", "Pakistan");
// print the function
console.log(city_country("Karachi", "Pakistan"));
// (2nd )            calling back function
city_country("Madina", "Saudia Arebia");
//print the function
console.log(city_country("Madina", "Saudia Arebia"));
// (3rd) calling back function
city_country("Beijing", "China");
// print the function
console.log(city_country("Beijing", "China"));
